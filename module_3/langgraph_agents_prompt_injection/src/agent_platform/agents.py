class MockLLMClient:
    """Deterministic local stand-in for an LLM provider.

    This keeps the project runnable in interviews without API keys while preserving
    the same agent boundary that a real LLM adapter would use.
    """

    def generate(self, prompt: str) -> str:
        """Return a deterministic response for traceability."""
        return f"Mock response for prompt length={len(prompt)}"


def load_prompt(path: Path) -> str:
    """Load a versioned prompt template from disk."""
    return path.read_text(encoding="utf-8")


class DeveloperAgent:
    """Generate or revise a structured Jupyter Notebook."""

    def __init__(self, llm: LLMClient, prompt_path: Path) -> None:
        self.llm = llm
        self.prompt_path = prompt_path

    def run(self, state: AgentState) -> AgentState:
        """Generate notebook JSON and extracted code."""
        task = state["task"]
        reviewer_feedback = state.get("reviewer_feedback", "")
        prompt = load_prompt(self.prompt_path)
        safe_task_block, injection_findings = build_untrusted_input_block(task)
        _ = self.llm.generate(
            f"{prompt}\n\nTASK_DATA:\n{safe_task_block}\n\n"
            f"REVIEWER_FEEDBACK:\n{reviewer_feedback}"
        )

        notebook = build_customer_name_notebook(task, reviewer_feedback)
        notebook_json = notebook_to_json(notebook)
        extracted_code = extract_code_cells(notebook_json)
        output_dir = Path(state.get("output_dir", "outputs"))

        write_notebook(notebook_json, output_dir / "developer_notebook.ipynb")
        write_module_from_code(extracted_code, output_dir / "generated_module.py")

        return {
            **state,
            "notebook_json": notebook_json,
            "extracted_code": extracted_code,
            "revision_count": state.get("revision_count", 0),
            "prompt_injection_findings": injection_findings,
        }


