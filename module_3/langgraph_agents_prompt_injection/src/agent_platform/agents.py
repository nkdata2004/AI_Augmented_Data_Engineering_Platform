class ReviewerAgent:
    """Review generated code and tests before commit."""

    def __init__(self, llm: LLMClient, prompt_path: Path) -> None:
        self.llm = llm
        self.prompt_path = prompt_path

    def run(self, state: AgentState) -> AgentState:
        """Produce a review report and final gate verdict."""
        prompt = load_prompt(self.prompt_path)
        _ = self.llm.generate(
            f"{prompt}\n\nCODE:\n{state['extracted_code']}\n\nTESTS:\n{state['test_file']}"
        )
        report, verdict = review_code_and_tests(
            state["extracted_code"],
            state["test_file"],
            state.get("prompt_injection_findings", []),
        )
        output_dir = Path(state.get("output_dir", "outputs"))
        output_dir.mkdir(parents=True, exist_ok=True)
        (output_dir / "review_report.md").write_text(report, encoding="utf-8")
        return {
            **state,
            "review_report": report,
            "verdict": verdict,
            "reviewer_feedback": report if verdict == "CHANGES_REQUESTED" else "",
        }
